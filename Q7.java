// The sliding window technique is a method for efficiently processing contiguous
// portions of a sequence (like an array or string). Instead of examining every
// possible sub-sequence, it maintains a "window" that moves through the data,
// processing only the elements within the window at any given time. This avoids
// redundant calculations.
// In string problems, the sliding window technique is useful for:
// ● Finding substrings that satisfy certain conditions (e.g., containing a specific
// set of characters).
// ● Calculating statistics on substrings (e.g., maximum sum of a substring of
// length K).
// ● Optimizing string matching and searching algorithms